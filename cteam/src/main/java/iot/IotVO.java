package iot;

public class IotVO {
	private String store_id, store_name, table_num, start_time, end_time, accupation_time;

	public String getStore_id() {
		return store_id;
	}

	public void setStore_id(String store_id) {
		this.store_id = store_id;
	}

	public String getStore_name() {
		return store_name;
	}

	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}

	public String getTable_num() {
		return table_num;
	}

	public void setTable_num(String table_num) {
		this.table_num = table_num;
	}

	public String getStart_time() {
		return start_time;
	}

	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}

	public String getEnd_time() {
		return end_time;
	}

	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}

	public String getAccupation_time() {
		return accupation_time;
	}

	public void setAccupation_time(String accupation_time) {
		this.accupation_time = accupation_time;
	}
	
	

}
